package com.mx.path.core.common.collection;

/**
 * Utility class representing relative ordering constants.
 */
public class Ordered {
  /**
   * Represents the highest priority or the first position in an ordered sequence.
   */
  public static final int FIRST = Integer.MIN_VALUE;
  /**
   * Represents an indifferent priority or neutral ordering.
   */
  public static final int INDIFFERENT = 0;
  /**
   * Represents the lowest priority or the last position in an ordered sequence.
   */
  public static final int LAST = Integer.MAX_VALUE;
}
